package basic.Aula8.Conversao;

public class ConversaoUnidade {

    public static double polegadaParaCentimetros(double polegadas) {
        return polegadas * 2.54;
    }

    public static double pesParaCentimetros(double pes) {
        return pes * 30.48;
    }

    public static double milhasParaQuilometros(double milhas) {
        return milhas * 1.609;
    }
    
}
